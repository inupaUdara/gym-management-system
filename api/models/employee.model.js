import mongoose from "mongoose";

const employeeSchema = new mongoose.Schema({
    firstname: {
        type: String,
        required: true,
    },
    lastname: {
        type: String,
        required: true,
    },
    username: {
        type: String,
        required: true,
        unique: true,
    },
    address: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    nic: {
        type: String,
        required: true,
        unique: true,
    },
    phone: {
        type: Number,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        default: "employee",
    },
    shift: {
        type: String,
        default: "none",
    },
    profilePicture: {
        type: String,
        default: "https://d1csarkz8obe9u.cloudfront.net/posterpreviews/profile-design-template-4c23db68ba79c4186fbd258aa06f48b3_screen.jpg",
    },
    isAdmin: {
        type: Boolean,
        default: false,
    },
}, { timestamps: true });

const Employee = mongoose.model("Employee", employeeSchema);
export default Employee;